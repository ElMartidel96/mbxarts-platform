// Default icon for unmapped emojis
export const defaultIcon = HelpCircle;

// Helper function to get Lucide icon for an emoji
export function getLucideIconForEmoji(emoji: string): LucideIcon {
  return emojiToLucideMap[emoji] || defaultIcon;
}

// Helper to check if a string contains emojis
export function containsEmoji(text: string): boolean {
  // Check if the text is in our mapping
  return Object.keys(emojiToLucideMap).some(emoji => text.includes(emoji));
}

// Helper to extract emojis from text
export function extractEmojis(text: string): string[] {
  const emojis: string[] = [];
  Object.keys(emojiToLucideMap).forEach(emoji => {
    if (text.includes(emoji)) {
      emojis.push(emoji);
    }
  });
  return emojis;
}

// Helper to replace emojis in text with icon placeholders
export function replaceEmojisWithPlaceholders(text: string): { text: string; emojis: string[] } {
  const emojis = extractEmojis(text);
  let processedText = text;
  
  emojis.forEach((emoji, index) => {
    processedText = processedText.replace(emoji, `__ICON_${index}__`);
  });
  
  return { text: processedText, emojis };
}